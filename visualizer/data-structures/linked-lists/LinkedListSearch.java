public class LinkedListSearch {
    static class Node {
        int data;
        Node next;
        
        Node(int data) {
            this.data = data;
            this.next = null;
        }
    }

    private Node head;

    public void insert(int data) {
        Node newNode = new Node(data);
        if (head == null) {
            head = newNode;
        } else {
            Node current = head;
            while (current.next != null) {
                current = current.next;
            }
            current.next = newNode;
        }
    }

    public boolean search(int data) {
        Node current = head;
        while (current != null) {
            if (current.data == data) {
                return true;
            }
            current = current.next;
        }
        return false;
    }

    public static void main(String[] args) {
        LinkedListSearch list = new LinkedListSearch();
        list.insert(1);
        list.insert(2);
        list.insert(3);
        
        System.out.println("Element 2 found: " + list.search(2));
        System.out.println("Element 4 found: " + list.search(4));
    }
}
